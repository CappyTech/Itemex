    private static boolean materialExists(String itemJson) {
        if (itemJson.startsWith("[{")) {
            String materialName = get_itemid(itemJson);
            //getLogger().info("# DEBUG MATERIALNAME: " + ItemexCommand.get_itemid(itemJson));
            return Material.getMaterial(materialName) != null;
        }
        return true;
    }



    public static void addIfMaterialExists(List<String> list, String item) {
        // Extract the material name from the string
        String materialName = item.substring(item.indexOf(":")+1, item.indexOf("\"", item.indexOf(":")+1)).toUpperCase();

        // Check if the material exists in the current server version
        if (Material.getMaterial(materialName) != null) {
            // If it exists, add the item to the list
            list.add(item);
        }
    }

